                if hasattr(message, 'reply_text') and callable(message.reply_text):
                     # –°–æ–∑–¥–∞–µ–º –æ–±—ä–µ–∫—Ç-–∑–∞–≥–ª—É—à–∫—É –¥–ª—è —Å–æ–æ–±—â–µ–Ω–∏—è —Å –¥–æ–ø. –∞—Ç—Ä–∏–±—É—Ç–æ–º
                     fake_message_obj = type('obj', (object,), {
                         'text': user_prompt,
                         'reply_text': message.reply_text, # –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–≤–µ—Ç–∞
                         'chat_id': chat_id,
                         'image_file_id': photo_file_id # –ü–µ—Ä–µ–¥–∞–µ–º file_id
                     })
