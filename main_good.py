    loop = asyncio.get_running_loop()
    stop_event = asyncio.Event()
    def signal_handler():
        if not stop_event.is_set(): logger.info("–ü–æ–ª—É—á–µ–Ω —Å–∏–≥–Ω–∞–ª SIGINT/SIGTERM, –æ—Å—Ç–∞–Ω–æ–≤–∫–∞..."); stop_event.set()
