    for sig in (signal.SIGINT, signal.SIGTERM):
        try: loop.add_signal_handler(sig, signal_handler)
        except NotImplementedError:
             logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ {sig} —á–µ—Ä–µ–∑ loop. –ò—Å–ø–æ–ª—å–∑—É—é signal.signal().")
             try: signal.signal(sig, lambda s, f: signal_handler())
             except Exception as e_signal: logger.error(f"–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ {sig} —á–µ—Ä–µ–∑ signal: {e_signal}")

    application = None
    web_server_task = None
    aiohttp_session_main = None
    try:
        logger.info(f"--- –ó–∞–ø—É—Å–∫ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è Gemini Telegram Bot ---")
        application, web_server_coro = await setup_bot_and_server(stop_event)
        web_server_task = asyncio.create_task(web_server_coro)
        aiohttp_session_main = application.bot_data.get('aiohttp_session')
        logger.info("–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–æ, –≤–µ–±-—Å–µ—Ä–≤–µ—Ä –∑–∞–ø—É—â–µ–Ω. –û–∂–∏–¥–∞–Ω–∏–µ —Å–∏–≥–Ω–∞–ª–∞ –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ (Ctrl+C)...")
        await stop_event.wait()
    except asyncio.CancelledError: logger.info("–ì–ª–∞–≤–Ω–∞—è –∑–∞–¥–∞—á–∞ main() –æ—Ç–º–µ–Ω–µ–Ω–∞.")
    except Exception as e: logger.critical("–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –¥–æ/–≤–æ –≤—Ä–µ–º—è –æ–∂–∏–¥–∞–Ω–∏—è.", exc_info=True)
    finally:
        logger.info("--- –ù–∞—á–∞–ª–æ –ø—Ä–æ—Ü–µ—Å—Å–∞ —à—Ç–∞—Ç–Ω–æ–π –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ ---")
        if not stop_event.is_set(): stop_event.set()
        if web_server_task and not web_server_task.done():
             logger.info("–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞...")
             try:
                 await asyncio.wait_for(web_server_task, timeout=15.0)
                 logger.info("–í–µ–±-—Å–µ—Ä–≤–µ—Ä –∑–∞–≤–µ—Ä—à–µ–Ω.")
             except asyncio.TimeoutError:
                 logger.warning("–í–µ–±-—Å–µ—Ä–≤–µ—Ä –Ω–µ –∑–∞–≤–µ—Ä—à–∏–ª—Å—è –∑–∞ 15—Å, –æ—Ç–º–µ–Ω–∞...")
                 web_server_task.cancel()
                 try: await web_server_task
                 except asyncio.CancelledError: logger.info("–ó–∞–¥–∞—á–∞ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞.")
                 except Exception as e_cancel: logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ–Ω–µ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞: {e_cancel}", exc_info=True)
             except asyncio.CancelledError: logger.info("–û–∂–∏–¥–∞–Ω–∏–µ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ –æ—Ç–º–µ–Ω–µ–Ω–æ.")
             except Exception as e_wait: logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–∂–∏–¥–∞–Ω–∏–∏ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞: {e_wait}", exc_info=True)
        if application:
            logger.info("–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è Telegram –±–æ—Ç–∞...")
            try: await application.shutdown(); logger.info("–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ Telegram –±–æ—Ç–∞ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ.")
            except Exception as e_shutdown: logger.error(f"–û—à–∏–±–∫–∞ application.shutdown(): {e_shutdown}", exc_info=True)
        if aiohttp_session_main and not aiohttp_session_main.closed:
             logger.info("–ó–∞–∫—Ä—ã—Ç–∏–µ —Å–µ—Å—Å–∏–∏ aiohttp...")
             await aiohttp_session_main.close()
             await asyncio.sleep(0.5)
             logger.info("–°–µ—Å—Å–∏—è aiohttp –∑–∞–∫—Ä—ã—Ç–∞.")
        tasks = [t for t in asyncio.all_tasks() if t is not asyncio.current_task()]
        if tasks:
            logger.info(f"–û—Ç–º–µ–Ω–∞ {len(tasks)} –æ—Å—Ç–∞–≤—à–∏—Ö—Å—è –∑–∞–¥–∞—á...")
            [task.cancel() for task in tasks]
            results = await asyncio.gather(*tasks, return_exceptions=True)
